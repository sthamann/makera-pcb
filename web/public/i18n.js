let LANG = 'de';
try {
  const saved = localStorage.getItem('makera_lang');
  if (saved === 'de' || saved === 'en') LANG = saved;
  else if ((navigator.language || '').toLowerCase().startsWith('en')) LANG = 'en';
} catch {}

export function getLang() { return LANG; }
export function setLang(l) {
  LANG = l === 'en' ? 'en' : 'de';
  try { localStorage.setItem('makera_lang', LANG); } catch {}
}
export function t(key, vars) {
  let s = (I18N[LANG] && I18N[LANG][key] != null) ? I18N[LANG][key] : (I18N.de[key] != null ? I18N.de[key] : key);
  if (vars) for (const k of Object.keys(vars)) s = s.split('{' + k + '}').join(vars[k]);
  return s;
}
export function applyI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.dataset.i18n); });
  root.querySelectorAll('[data-i18n-html]').forEach((el) => { el.innerHTML = t(el.dataset.i18nHtml); });
  root.querySelectorAll('[data-i18n-title]').forEach((el) => { el.title = t(el.dataset.i18nTitle); });
  root.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.placeholder = t(el.dataset.i18nPh); });
  root.querySelectorAll('[data-i18n-tip]').forEach((el) => { el.setAttribute('data-tip', t(el.dataset.i18nTip)); });
  document.documentElement.lang = LANG;
  document.title = t('app.title');
}
